import java.util.Stack;

/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public int[] reversePrint(ListNode head) {
        Stack<Integer> st = new Stack<>();
        int len = 0;
        while (head != null) {
            st.push(head.val);
            len++;
            head = head.next;
        }
        int[] res = new int[len];
        int i = 0;
        while (!st.empty()) {
            res[i++] = st.pop();
        }
        return res;
    }
}
